// Find all the even numbers from 78 to 98.

console.log("All the odd numbers from 61 to 100:");
for (let i = 61; i <= 100; i++) {
    if (i % 2 === 1) {
        console.log(i);
    }
}

console.log("\n");

console.log("All the even numbers from 78 to 98:");
for (let j = 78; j <= 98; j++) {
    if (j % 2 === 0) {
        console.log(j);
    }
}