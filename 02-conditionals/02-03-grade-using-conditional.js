let number = 69;
if (number >= 80) {
    console.log("You have got A+");
}
else if (number >= 70) {
    console.log("You have got A");
}
else if (number >= 60) {
    console.log("You have got A-");
}
else if (number >= 50) {
    console.log("You have got B");
}
else if (number >= 40) {
    console.log("You have got C");
}
else if (number >= 33) {
    console.log("You have got D");
}
else {
    console.log("You are failed!");
}